def add(x, y):
    """
    Add two numbers

    Parameters
    ----------
    x : numeric
        First number to be added
    y : numeric
        Second number to be added
    """
    return x+y

def substract(x, y):
    """
    Substract two numbers

    Parameters
    ----------
    x : numeric
        First number to be substracted from 
    y : numeric
        Second number to be substracted
    """
    return x-y